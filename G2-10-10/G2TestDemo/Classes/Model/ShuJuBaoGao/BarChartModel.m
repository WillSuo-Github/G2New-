#import "BarChartModel.h"

@implementation BarChartModel

- (instancetype) initWithDictionary:(NSDictionary *)dic{
    if (self = [super init]) {
        [self setValuesForKeysWithDictionary:dic];
    }
    return self;
}
- (void)setValuesForKeysWithDictionary:(NSDictionary *)dic{
    _totalmoney = [dic[@"totalmoney"] integerValue];
    _paymentName = dic[@"paymentName"];
    _cptIdCount = dic[@"cptIdCount"];
}

@end
