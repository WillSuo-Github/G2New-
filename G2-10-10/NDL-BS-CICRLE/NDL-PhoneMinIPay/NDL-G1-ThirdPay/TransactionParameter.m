#import "TransactionParameter.h"

@implementation TransactionParameter


/**
 * 
 *
 *  /**
 *  out_trade_no  商户唯一订单号
 auth_code 扫码枪扫描到的用户的付款条码
 total_amount 订单总金额（微信支付为分，支付宝支付为元）
 subject 商品或支付单简要描述
 pay_type支付方式（ali_pay：支付宝支付,qq_pay：qq钱包,weixin_pay：微信支付,baidu_pay：百度钱包）
 merchant_no 商户号（腾势管理的商户的id）
 appl_id 应用标识;
 */

-(instancetype)init
{
    if (self = [super init]) {
        self.out_trade_no = nil;
        self.auth_code = nil;
        self.total_amount = nil;
        self.subject = nil;
        self.pay_type = nil;
        self.merchant_no = nil;
        self.appl_id = nil;
        self.payee_phone = nil;
        return self;
    }
    
    return nil;
    
}




@end
